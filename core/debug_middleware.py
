import os
import json
import subprocess

PROJECT_PATH = "/home/edson/imobiliaria_str/core-app"

print("\n🔎 DIAGNÓSTICO DE MIDDLEWARE NEXT.JS\n")

# --------------------------------------------------
# 1 Verificar existência do middleware
# --------------------------------------------------

possible_paths = [
    f"{PROJECT_PATH}/middleware.ts",
    f"{PROJECT_PATH}/src/middleware.ts"
]

middleware_path = None

for p in possible_paths:
    if os.path.exists(p):
        middleware_path = p
        break

if middleware_path:
    print("✅ Middleware encontrado em:")
    print("   ", middleware_path)
else:
    print("❌ Middleware NÃO encontrado")
    exit()

# --------------------------------------------------
# 2 Verificar tamanho do arquivo
# --------------------------------------------------

size = os.path.getsize(middleware_path)
print(f"\n📄 Tamanho do middleware: {size} bytes")

# --------------------------------------------------
# 3 Procurar linhas suspeitas
# --------------------------------------------------

print("\n🔍 Verificando linhas suspeitas...")

with open(middleware_path) as f:
    lines = f.readlines()

for i,l in enumerate(lines):
    if "edson@" in l or "$" in l:
        print(f"⚠ Linha suspeita {i+1}: {l.strip()}")

# --------------------------------------------------
# 4 Verificar matcher
# --------------------------------------------------

matcher_found = False

for l in lines:
    if "matcher" in l:
        matcher_found = True

if matcher_found:
    print("✅ Matcher encontrado no middleware")
else:
    print("⚠ Matcher não encontrado")

# --------------------------------------------------
# 5 Verificar middleware manifest
# --------------------------------------------------

manifest = f"{PROJECT_PATH}/.next/server/middleware-manifest.json"

if os.path.exists(manifest):
    with open(manifest) as f:
        data = json.load(f)

    if data.get("middleware"):
        print("\n✅ Middleware detectado no build")
    else:
        print("\n❌ Middleware NÃO detectado no build")
else:
    print("\n⚠ middleware-manifest.json não encontrado")

# --------------------------------------------------
# 6 Testar TypeScript
# --------------------------------------------------

print("\n🧪 Executando verificação TypeScript...")

try:
    result = subprocess.run(
        ["npx","tsc","--noEmit"],
        cwd=PROJECT_PATH,
        capture_output=True,
        text=True
    )

    if result.returncode == 0:
        print("✅ TypeScript OK")
    else:
        print("❌ Erros TypeScript:")
        print(result.stdout)

except Exception as e:
    print("⚠ Falha ao rodar tsc:", e)

# --------------------------------------------------
# 7 Estrutura do projeto
# --------------------------------------------------

print("\n📁 Estrutura principal do projeto:\n")

for item in os.listdir(PROJECT_PATH):
    print(" ",item)

print("\n🏁 Diagnóstico concluído\n")

